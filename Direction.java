/**
 * Represents the direction in which an elevator can move.
 * This enum defines the possible directions an elevator can travel within a building:
 * 
 *     UP - Indicates the elevator is moving upwards.</li>
 *     DOWN - Indicates the elevator is moving downwards.</li>
 *     STOP - Indicates the elevator is not moving and is at a standstill.</li>
 */
public enum Direction {
    /**
     * The elevator is moving upwards.
     */
    UP,

    /**
     * The elevator is moving downwards.
     */
    DOWN,

    /**
     * The elevator is not moving and is at a standstill.
     */
    STOP
}